// nav bar
const iconNav = document.querySelector('.nav-icon');
if (iconNav) {
  const navBody = document.querySelector('.nav-body');
  iconNav.addEventListener("click", function (e) {
    document.body.classList.toggle('_lock');
    iconNav.classList.toggle('_active');
    navBody.classList.toggle('_active');
  });
}


document.addEventListener("DOMContentLoaded", function () {
      var scrollbar = document.body.clientWidth - window.innerWidth + 'px';
      console.log(scrollbar);
      document.querySelector('[href="#openModal-1"]').addEventListener('click', function () {
        document.body.style.overflow = 'hidden';
        document.querySelector('#openModal-1').style.marginLeft = scrollbar;
      });
      document.querySelector('[href="#close-1"]').addEventListener('click', function () {
        document.body.style.overflow = 'visible';
        document.querySelector('#openModal-1').style.marginLeft = '0px';
      });
});
    
